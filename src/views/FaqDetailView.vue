<template>
  <div class="container-fluid kb-view">
    <div class="row">
      <p></p>
    </div>
    <div class="row">
      <div class="col-1"></div>
      <div class="col-10">
        <div class="row">
          <p></p>
        </div>
        <div class="row">
          <div class="col-12 title-view">Faq Detail</div>
        </div>
        <div class="row">
          <p></p>
        </div>
        <div class="row">
          <div class="col-7">
            <MainQuestionComponent :text="mainQuestion" :id="id"></MainQuestionComponent>
          </div>
          <div class="col-1"></div>
          <div class="col-4">
            <FaqDetailCommandsComponent :trained="trained" :id="id"></FaqDetailCommandsComponent>
          </div>
        </div>
        <div class="row">
          <p></p>
        </div>
        <div v-for="(example, lang) in examples" :key="lang">
          <FaqLanguageExamplesComponent :id="id" :displayLang="languages[lang]" :examples="example"></FaqLanguageExamplesComponent>
          <div class="row">
            <p></p>
          </div>
        </div>
      </div>
      <div class="col-1"></div>
    </div>
    <div class="row">
      <p></p>
    </div>
  </div>
</template>

<script>
import axios from "axios";
import MainQuestionComponent from "./../components/MainQuestionComponent.vue";
import FaqDetailCommandsComponent from "./../components/FaqDetailCommandsComponent.vue";
import FaqLanguageExamplesComponent from "./../components/FaqLanguageExamplesComponent.vue";

export default {
  name: "FaqDetailView",
  components: {
    MainQuestionComponent,
    FaqDetailCommandsComponent,
    FaqLanguageExamplesComponent
  },
  data() {
    return {
      id: "",
      urlBackend: global.config.backendUrl +
        global.config.backendPort +
        global.config.backendApiPath +
        global.config.getFaqsEndpoint,
			urlBackendLanguage:
				global.config.backendUrl +
				global.config.backendPort +
				global.config.backendApiPath +
				global.config.getLanguageEndpoint,
      mainQuestion: "",
      examples: [],
	  trained: false,
	  languages: {}
    };
  },
  created(){
    this.getDisplayLanguages();
  },
  mounted() {
    this.id = this.$route.params.id;
    this.getFaqDetails();

    this.$eventHub.$on("changeTrainingStatus", this.changeTrainingStatus);
    this.$eventHub.$on("deleteExample", this.deleteExample);
    this.$eventHub.$on("deleteFaq", this.deleteFaq);
    this.$eventHub.$on("newExample", this.addNewExample);
    this.$eventHub.$on("newMainQuestion", this.setNewMainQuestion);
    this.$eventHub.$on("saveFaq", this.saveFaq);
  },
  methods: {
    addNewExample(obj) {
      let that = this;
      that.examples[obj.lang].push(obj.example);
    },
    changeTrainingStatus() {
      this.trained = !this.trained;
    },
    deleteExample(obj) {
      let that = this;

      var index = that.examples[obj.lang].indexOf(obj.example);
      if (index !== -1) that.examples[obj.lang].splice(index, 1);
    },
    async deleteFaq() {
      var that = this;
      var parameters = {
        params: {
          id: that.id
        }
      };
      await axios
        .delete(that.urlBackend, parameters)
        .then(res => {
          this.$router.push({ name: "Knowledge Base" });
        })
        .catch(err => {
          console.log(err);
        });
	},
	async getDisplayLanguages(){
		let that = this;
		await axios
			.get(this.urlBackendLanguage)
			.then(res => {
					that.languages = res.data.languages;
				})
			.catch(err => {
				console.log(err);
			});
	},
    async getFaqDetails() {
      let that = this;
      await axios
        .get(this.urlBackend, {
          params: { id: that.id }
        })
        .then(function(response) {
          that.mainQuestion = response.data.mainQuestion;
          that.examples = response.data.examples;
          that.id = response.data.id;
          that.trained = response.data.trained;
        })
        .catch(function(error) {
          console.log(error);
        });
    },
    async saveFaq() {
      let that = this;
      let body = {
          id: that.id,
          mainQuestion: that.mainQuestion,
          examples: that.examples,
          trained: that.trained
        };

      await axios
        .put(this.urlBackend, body)
        .then(function(response) {
          that.$router.push({ name: "Knowledge Base" });
        })
        .catch(function(error) {
          console.log(error);
        });
    },
    setNewMainQuestion(question) {
      let that = this;
      that.mainQuestion = question;
    }
  }
};
</script>